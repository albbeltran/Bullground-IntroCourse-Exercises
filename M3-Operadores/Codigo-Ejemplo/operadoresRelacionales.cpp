// Operadores relacionales

// Si la relacion es verdadera, retorna un 1, si es falsa un 0.

#include<iostream>

using namespace std;

int main() {
	int a = 5;
	int b = 10;
	
	cout<< (a > b) <<endl;
	
	cout<< (a < b) <<endl;
	
	cout<< (a >= b) <<endl;
	
	cout<< (a <= b) <<endl;
	
	cout<< (a == b) <<endl;
	
	cout<< (a != b) <<endl;
	
	return 0;
}
